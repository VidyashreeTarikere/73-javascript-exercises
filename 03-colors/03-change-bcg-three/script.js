(function() {

    // your code here
    var mycolor="";
    // your code here
    
    document.getElementById("run").addEventListener("click", function() {
        function random_rgba() {
            var o = Math.round, r = Math.random, s = 255;
            return 'rgba(' + o(r()*s) + ',' + o(r()*s) + ',' + o(r()*s) + ',' + r().toFixed(1) + ')';
        }
        
        mycolor = random_rgba();
        document.body.style.backgroundColor = mycolor;
    });

})();
